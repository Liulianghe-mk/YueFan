function getMeetDetail(id) {
  var n = parseInt(id, 10);
  function augment(d) {
    var need = Math.max(0, d.total - d.joined);
    var p = d.pricePerPerson;
    var priceText =
      typeof p === 'number' && !isNaN(p) ? '¥' + p + '/人' : d.priceText || '¥待定/人';
    return Object.assign({}, d, { needMore: need, priceText: priceText });
  }
  if (!isNaN(n) && DETAILS[n]) {
    return augment(DETAILS[n]);
  }
  var d3 = DETAILS[3];
  return augment(d3);
}

